
numbers_list = [1, 2, 3, 4, 5, 2, 5, 9, 7, 8, 9, 9, 1, 2]

# index
print(numbers_list.index(3)) # 2, esto indica la posicion en la cual se encuentra el numero [2]

# in 
print(2 in numbers_list) # True, esto ayuda a realizar busquedas de forma mas efectiva y rapida. Regresa un Truo o False como respuesta.

# Count
print(numbers_list.count(2)) # 3, esta funcion se encarga de contar cuantas veces se encuenta la funcion en el sistema.